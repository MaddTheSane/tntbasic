#include	"Marks Routines.h"
#include	<StdIO.h>

void InputString(StringPtr theString)
{
	gets((char *)theString);
}

void PrintPString(StringPtr string)
{
	Str255	temp;
	
	CopyPAsCStr(string,temp);
	printf("%s\n",temp);
}

void PrintStr(short res,short index)
{
	Str255	temp;
	GetIndString(temp,res,index);
	PrintPString(temp);
}

void InputNumber(long *theNum)
{
	Str255	theString;

	InputString(theString);
	CStrToPStr(theString);
	StringToNum(theString,theNum);
}