// Changes the file which is displayed in the text box
void CFilePathPane::SetFile(
	FSSpec		*inSpec)
{
	if (inSpec)
	{
		Handle	fullPath;
		short	length;
	
		if (OSErr err=FSpGetFullPath(inSpec,&length,&fullPath))
			SetDescriptor("\pCan't get file path.");
		else
		{
			UHandleReleaser		rel(fullPath);
			UHandleLocker			lock(fullPath);
			
			SetText(*fullPath,length);
		}
	}
	else
		SetDescriptor("\p");
}
