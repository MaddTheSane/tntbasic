// ÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑ
void CIconReference::DrawSelf()
{
	Rect		rect;
	
	if (CalcLocalFrameRect(rect))
		::PlotIconRef(&rect,mIconAlignment,mIconTransform | (IsActive() ? 0 : kTransformDisabled),kIconServicesNormalUsageFlag,mIconRef);
}

