// You can specify a null inModelObject if you want and specify it later using SetModelObjet
CModelAction::CModelAction(
	LModelObject			*inModelObject,
	ResIDT					inStringResId,
	SInt16					inStringIndex,
	bool					inAlreadyDone) :
	LAction(inStringResId,inStringIndex,inAlreadyDone)
{
	mModelObject.descriptorType=typeNull;
	mModelObject.dataHandle=0L;
	SetModelObject(inModelObject);
}

