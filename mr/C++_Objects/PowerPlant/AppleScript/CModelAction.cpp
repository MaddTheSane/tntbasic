// ÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑ
// Resolves the specifier to get the model object to which it refers, this is slow so cache the LModelObject ptr for as long
// as you can be sure it is valid.
LModelObject *CModelAction::GetModelObject()
{
	StAEDescriptor		token;

	ThrowIfOSErr_(LModelDirector::GetModelDirector()->Resolve(mModelObject,token));
	return LModelObject::GetModelFromToken(token);
}

