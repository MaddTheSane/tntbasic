// Lets all sub models know the super has died.
CModelObjectWithSubList::~CModelObjectWithSubList()
{	
	if (mSubModelsList!=0L)
	{
		TListIndexerT<CModelObjectPlus>		indexer(mSubModelsList,false);
	
		LModelObject*	sub;
		
		while (sub=indexer.GetPrevData())
		{
			sub->SetSuperModel(0L);
			sub->SuperDeleted();
		}		

		if (mOwnsList)
			delete mSubModelsList;
	}
}

