// ÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑ
// Gets the chan if it exists, returns 0 if not
CProgMoanChannel *CMoanManager::GetChannelNoCreate(
	SInt32			inId)
{
	CListIndexerT<CProgMoanChannel>		mIndexer(&mChannels);
	
	while (CProgMoanChannel *chan=mIndexer.GetNextData())
	{
		if (chan->GetId()==inId)
			return chan;
	}

	return 0;
}

