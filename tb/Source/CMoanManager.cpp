// Gets the channel of the id specified, creates the channel if it doesn't exist
CProgMoanChannel *CMoanManager::GetChannel(
	CProgram	&ioState,
	SInt32		inId)
{
	// No channel, create it
	CProgMoanChannel		*p=GetChannelNoCreate(inId);
	
	if (p)
		return p;
	
	p=new CProgMoanChannel(&ioState,inId);
	ThrowIfMemFull_(p);
	
	mChannels.LinkElement(&p->mLink);
	
	return p;
}

