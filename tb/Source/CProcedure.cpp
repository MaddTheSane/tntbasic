// Returns true if the symbol is a parameter
bool CProcedure::IsParameter(
	CVariableSymbol		*inSymbol)
{
	for (CVariableNode *node=mParameters; node; node=node->TailData())
	{
		if (node->mSymbol==inSymbol)
			return true;
	}
	return false;
}

