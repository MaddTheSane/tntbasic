// Sets the parameters for a procedure. When setting them it checks if any of them are globals and if so throws as you can't
// recieve parameters into a global. This is done to conform with not being able to share parameters and THAT is done as
// the parameter (a symbol in the procedures symbol table) is deleted and replaced by a global one and so the parameter
// list would need updating to refer to the new symbols and it's not worth the work to do that as it would be useless anyway.
void CProcedure::SetParameters(
	const STextRange	&inRange,
	CVariableNode		*inParameters)
{
	mParameters=inParameters;

	for (CVariableNode *node=inParameters; node; node=node->TailData())
	{
		// Check if it's a global
		if (mIdentifierScope.VarTable()->IsGuest(node->mSymbol))
			UTBException::ThrowCantUseGlobalAsParameter(inRange);		
	}
}
