//		¥ SetReturnValues
// ÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑ
void CStackFrame::SetReturnValues(
	TTBInteger		inInt,
	TTBFloat		inFloat,
	const char		*inStr)
{
	mFrameResult=inInt;
	mFrameResultF=inFloat;
	mFrameStrResult.Assign(inStr);
}

