/*
	A stack frame is stored in the execution stack when a procedure or gosub is called. It stores the variable value table
	(ie variable memory) for the current procudure call and also the return address for when the call terminates.
	
	It also stores the result from the frames execution which is how values are passed back to the caller in procedures.
*/

#include		"CStackFrame.h"
#include		"CIdentifierScope.h"

