CStackFrame::CStackFrame(
	EFrameType			inFrameType,
	CStatement			*inReturnAddress,
	CGeneralExpression		*inReturnDataStatement,
	CIdentifierScope	&inScope) :
	CListElementT<CStackFrame>(this),
	mStorage(*inScope.VarTable()),
	mFrameType(inFrameType),
	mReturnAddress(inReturnAddress),
	mReturnDataAddress(inReturnDataStatement),
	mFrameResult(0)
{
}

