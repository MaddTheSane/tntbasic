#pragma once

#include "CBundleResFileCompat.h"
#include "CImageBank.h"

class CTBResClassifier : public CBundleResFileCompatClassifier
{
	public:
		// handle image masks being seen as the same as picts
		virtual bool /*e*/				TypesEquiv(
											ResType			inType1,
											ResType			inType2)
										{
											return 	(inType1=='PICT' && inType2==kBLImageMaskType) ||
													(inType2=='PICT' && inType1==kBLImageMaskType);
										}
};