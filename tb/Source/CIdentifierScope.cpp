// Call this when all entries to the table are complete. It then assigns a unique id to all the entries meaning they can
// access a value table. Any further attempts to add identifiers to the table will cause an error.
void CIdentifierScope::EntriesComplete()
{
	mVariablesTable.TableComplete();
	mLabelTable.TableComplete();
}

// ÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑ
//		¥ÊMakeEntriesRoot
// ÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑÑ
// Makes all symbols in this procedure be root references
void CIdentifierScope::MakeEntriesRoot()
{
	ArrayIndexT			max,index;
	
	max=mVariablesTable.CountEntries();
	for (index=0; index<max; index++)
		mVariablesTable.GetNthEntry(index)->MakeRoot();
}

