// This is used to add global variables to a parsing scope as guests.
// The inGuestsList vars are all assumed to be symbols as they've been checked by CProgram::AddGlobals
void CIdentifierScope::AddGuests(
	CVariableNode	*inGuestsList)
{
	for (CVariableNode *node=inGuestsList; node; node=node->TailData())
		mVariablesTable.EnterGuest(node->mSymbol);
}

