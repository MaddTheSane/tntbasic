// Increase size of runtime stack by 100 elements
void CVMStack::GrowStack()
{
	::HUnlock(mStackHandle);
	Size		newSize=::GetHandleSize(mStackHandle)+kIncreaseBy;
	
	::SetHandleSize(mStackHandle,newSize);
	if (GetHandleSize(mStackHandle)!=newSize)
	{
		OSErr		err=::MemError();
		if (!err)
			err=memFullErr;
		Throw_(err);
	}
	mMaxSP+=kIncreaseBy/sizeof(SVMStackElement);
	
	ECHO("Stack size increased to " << ((UInt32)mMaxSP) << " bytes");
	::HLockHi(mStackHandle);
}
