CVMStack::CVMStack()
{
	mStackHandle=::NewHandle(sizeof(SVMStackElement)*500);		// 500 variables
	mSP=0;
	mMaxSP=(SVMStackElement*)(sizeof(SVMStackElement)*500);		// offset into handle
	
	::HLockHi(mStackHandle);									// better for quick access if locked
}

